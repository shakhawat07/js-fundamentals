// if-else 
var color = 'white';

if (color == 'red') {
    console.log('Color is Red')
}
else if (color == 'yellow') {
    console.log('Color is Yellow')
}
else if (color == 'green') {
    console.log('Color is Green')
}
else {
    console.log('Color is White')
}
// switch 
switch (color) {
    case 'red':
        console.log('Color is Red');
        break;
    case 'yellow':
        console.log('Color is Yellow');
        break;
    case 'green':
        console.log('Color is Green');
        break;
    default:
        console.log('Color is White');
}